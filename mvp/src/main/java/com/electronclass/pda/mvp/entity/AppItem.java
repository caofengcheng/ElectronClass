package com.electronclass.pda.mvp.entity;



public class AppItem {

    private String name;
    private int code;
    private int image;

    public String getName() {
        return name;
    }

    public int getCode() {
        return code;
    }

    public int getImage() {
        return image;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public void setImage(int image) {
        this.image = image;
    }

}
